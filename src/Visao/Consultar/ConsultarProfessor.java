     private void AtualizaTable(){
        Connection con = Conexao.AbrirConexao();
        professorDAO sql = new professorDAO(con);
        List<professor> lista = new ArrayList<>();
        lista =  sql.ListarProfessor();
        DefaultTableModel tbm = (DefaultTableModel) jTable1.getModel();
        while (tbm.getRowCount() > 0){
            tbm.removeRow(0);
        }
        int i = 0;
        for(professor tab : lista){
            tbm.addRow(new String [i]);
            jTable1.setValueAt(tab.getCod(), i, 0);
            jTable1.setValueAt(tab.getNome(), i, 1);
            jTable1.setValueAt(tab.getEmail(), i, 2);
            jTable1.setValueAt(tab.getCpf(), i, 3);
            jTable1.setValueAt(tab.getTelefone(), i, 4);
            jTable1.setValueAt(tab.getEndereco(), i, 5);
            i++;
        }
        Conexao.FecharConexao(con);
    }
